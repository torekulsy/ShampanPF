        /// <summary>
        /// Retrieves a list of active journal types from the EnumJournalType database table.
        /// </summary>
        /// <returns>
        /// A list of EnumJournalTypeVM objects representing the active journal types, ordered by Name.
        /// </returns>
        public List<EnumJournalTypeVM> JournalTypeDropDown()
        {
            #region Variables
            SqlConnection currConn = null;
            string sqlText = "";
            List<EnumJournalTypeVM> VMs = new List<EnumJournalTypeVM>();
            EnumJournalTypeVM vm;
            #endregion
            try
            {
                #region open connection and transaction
                currConn = _dbsqlConnection.GetConnection();
                if (currConn.State != ConnectionState.Open)
                {
                    currConn.Open();
                }
                #endregion open connection and transaction
                #region sql statement
                sqlText = @"
SELECT
Id
,Name
   FROM EnumJournalType
WHERE  1=1 and IsActice=1
";

                sqlText = sqlText + " ORDER BY Name";

                SqlCommand objComm = new SqlCommand(sqlText, currConn);


                SqlDataReader dr;
                dr = objComm.ExecuteReader();
                while (dr.Read())
                {
                    vm = new EnumJournalTypeVM();
                    vm.Id = Convert.ToInt32(dr["Id"]);
                    vm.Name = dr["Name"].ToString();
                    VMs.Add(vm);
                }
                dr.Close();
                #endregion
            }
            #region catch
            catch (SqlException sqlex)
            {
                throw new ArgumentNullException("", "SQL:" + sqlText + FieldDelimeter + sqlex.Message.ToString());
            }
            catch (Exception ex)
            {
                throw new ArgumentNullException("", "SQL:" + sqlText + FieldDelimeter + ex.Message.ToString());
            }
            #endregion
            #region finally
            finally
            {
                if (currConn != null)
                {
                    if (currConn.State == ConnectionState.Open)
                    {
                        currConn.Close();
                    }
                }
            }
            #endregion
            return VMs;
        }

